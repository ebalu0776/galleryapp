class Gallery extends Component {
  state = {
    stateImage: imagesList[0].id,
  }

  clickImageItem = () => {
    this.setState(prevState => ({stateImage: prevState.stateImage + 1}))
  }

  onFilterImages = () => {
    const {stateImage} = this.state
    const filterImages = imagesList.filter(
      eachImage => eachImage.id === stateImage,
    )
    return filterImages
  }

  render() {
    const filteredImages = this.onFilterImages()
    const {imageUrl, thumbnailUrl} = this.props
    return (
      <div className="gallery-container">
        <h1 className="heading">Nature Photography</h1>
        <p className="paragraph">Nature Photography by Rahul</p>

        <ul>
          {filteredImages.map(eachImg => (
            <ThumbnailItem
              imageDetails={eachImg}
              key={eachImg.id}
              clickImageItem={this.clickImageItem}
              imageUrl={imageUrl}
              thumbnailUrl={thumbnailUrl}
            />
          ))}
        </ul>
      </div>
    )
  }
}
export default Gallery
